# Basic Arithmetic

# Addition
print(25 + 50)

# Subtraction
print(50 - 25)

# Multiplication
print(20 * 5)

# Division
print(100 / 5)

# Division with decimals
print(65 / 14)

# Floor Division, It will eliminate all the decimals
print(65 // 14)

# Modulo % modulo operator will get you the remainder
print(7 % 4)

# Powers ** to the power
print(2 ** 3) # 2 * 2 * 2 = 8
print(2 ** 4) # 2 * 2 * 2 * 2 = 16

# Roots of a number
print(25 ** 0.5)
print(16 ** 0.5)

# Order of Operations in Python
print(5 + 10 * 10 + 3) # 108

# We can use parenthesis to specify orders
print((5 + 10) * (10 + 3)) # 195

# type() function returns me the data type of my variable
print(type(10)) # int for integer values
print(type(4.5)) # floating points are values with decimal point like

# Variable Assignments
x = 3
print(x)

print(x + x)

x = 81
print(x)

y = x
print(y)



