# Rules for Variable Names
# Variable names can't start with a number
# Variable names can't contain spaces, instead use underscores
# Variable names can't contain any of the symbols: '<?!@

my_cats = 4

print(my_cats)

my_cats = 6

print(my_cats)


b = 10

# Reassigning variables

b = b + b + b
print(b)

c = 20
# c = c + 10
# There is a shortcut for writing the code above
# +=
c += 10 # c = c + 10
print(c)

a = 4
a *= 2 # a = a * 2
print(a)

y = 25
y /= 5
print(y)

x = 30
x -= 10 # x = x - 10
print(x)






