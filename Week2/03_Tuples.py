# Empty Tuple
my_tuple = ()
print(type(my_tuple))
print(my_tuple)

# Tuple having numbers
my_tuple_1 = (1, 2, 3)
print(my_tuple_1)

# Tuple with mixed datatypes
my_tuple_2 = (1, "Hello", 3.14)
print(my_tuple_2)

# Nested Tuple
my_tuple_3 = ("Lion", [10, 20, 30], (1, 2, 3), 3.14)
print(my_tuple_3)

# Creating a tuple with single element
my_tuple_4 = ("Hello") # str
print(type(my_tuple_4))

# Creating a tuple with single element
my_tuple_5 = ("HI",) # tuple
print(type(my_tuple_5))

# snake_case

my_tuple_6 = "hello", "cemil", 2, (1, 2, 3)
print(my_tuple_6)
print(type(my_tuple_6))

